// Write a java program to display strings in uppercase, lowercase and length of the string.

public class Eight {
    public static void main(String[] args) {
        String myStr = "Anurag Vishwakarma";
        System.out.println("Uppercase: " + myStr.toUpperCase());
        System.out.println("Lowercase: " + myStr.toLowerCase());
        System.out.println("Length: " + myStr.length());
    }
}
